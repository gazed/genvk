// Generate interface for a command.
func (gen *generator) genCmd(info Info, cmdName, alias string) {
	cmdinfo, _ := info.(*CmdInfo)
	proto := cmdinfo.elem.FindElement("proto")
	cmd := &Cmd{Mems: []Member{}}
	cmd.Name = gen.mapVariable(proto.FindElement("name").Text())
	cmd.Type = gen.mapVariable(proto.FindElement("type").Text())
	cmd.Doc = docLink("cmd", cmd.Name)

	// output the command parameters.
	for _, param := range cmdinfo.elem.FindElements("param") {
		cmem := Member{}
		cmem.Name = gen.mapVariable(param.FindElement("name").Text())
		typeElem := param.FindElement("type")
		typeText := typeElem.Text()
		paramTail := param.Tail()
		paramText := param.Text()
		cmem.Type = gen.mapType(typeText)

		// refine the member length information.
		cmem.Len = param.SelectAttrValue("len", "")
		if cmem.Len != "" {
			// println("=-==== len", cmem.Len)
		}
		if strings.Contains(cmem.Len, "null-terminated") {
			// println("len is null terminated", cmem.Len)
			cmem.IsNullTerminated = true
		}
		if strings.Contains(cmem.Len, "->") {
			// Parameter is binding-allocated array populated by Vulkan;
			// length is embedded in a struct ie: pAllocateInfo->commandBufferCount
			cmem.IsBindingAllocatedArray = true
			cmem.Len = strings.Replace(cmem.Len, "->", ".", 1)
		}
		switch {
		case strings.HasPrefix(typeElem.Tail(), "** "):
			cmem.Ptr = "**"
			cmem.IsSingleReturn = true
		case strings.HasPrefix(typeElem.Tail(), "* "):
			cmem.Ptr = "*"
			isConst := strings.HasPrefix(paramTail, "const") || strings.HasPrefix(paramText, "const")
			isReturn := !isConst
			switch {
			case cmem.Type == "byte" && typeText == "char" && cmem.IsNullTerminated:
				cmem.IsString = true
			case cmem.Type == "unsafe.Pointer" && typeText == "void" && cmem.Ptr == "*":
				cmem.IsByteArray = true
			case isReturn:
				switch {
				case cmem.Len != "":
					cmem.IsSliceReturn = true
				default:
					cmem.IsSingleReturn = true
				}
			default:
				// standard pointers are handled in apigen
			}
		default:
			// standard vars are handled in apigen
		}
		cmd.Mems = append(cmd.Mems, cmem)
	}

	// look for counts + arrays members that can be converted to slices.
	for i, mem := range cmd.Mems {
		for j, m2 := range cmd.Mems {

			// found an slice candidate field that references count field as its length.
			if mem.Len == m2.Name {
				// println("slice", cmd.Name, mem.Name)
				cmd.Mems[i].IsSliceField = true
			}

			// this member is referenced as the "Len" field in another member.
			// By convention this field ends with a "Count"
			if mem.Name == m2.Len {
				// println("counter", cmd.Name, mem.Name)
				cmd.Mems[i].IsSliceCounter = true
				cmd.Mems[j].lenMems = append(cmd.Mems[j].lenMems, &cmd.Mems[i])
				if mem.Ptr == "*" {
					// counter is a pointer so that it can return the number
					// of elements when the array element is passed in as nil.
					// This requires calling the command twice, once to get
					// the array size, and the second call to fill the array.
					cmd.IsDoubleCall = true
				}
			}
		}
	}

	// println("generated command", gen.platform, cmd.Name)
	isCgo, isSys := true, false //
	switch {
	case gen.file == baseFile:
		gen.file = cgoFile
		gen.write(cmdSection, "") // init buffer
		writeCmd(cmd, isCgo, gen.out[cgoFile][cmdSection])
		gen.file = syscallFile
		gen.write(cmdSection, "") // init buffer
		writeCmd(cmd, isSys, gen.out[syscallFile][cmdSection])
		gen.file = baseFile
	case strings.Contains(gen.file, "win32"):
		gen.write(cmdSection, "") // init buffer
		writeCmd(cmd, isSys, gen.out[gen.file][cmdSection])
	default:
		gen.write(cmdSection, "") // init buffer
		writeCmd(cmd, isCgo, gen.out[gen.file][cmdSection])
	}
}

// Called once when starting generated code output.
// Start a new interface file
func (gen *generator) beginFile() {
	gen.file = baseFile

	// inject the handle type definitions
	s := defineSection
	gen.write(s, docLink("HANDLE", "VK_DEFINE_HANDLE")+"\n")
	gen.write(s, "type handle uintptr\n\n")
	gen.write(s, docLink("HANDLE", "VK_DEFINE_NON_DISPATCHABLE_HANDLE")+"\n")
	gen.write(s, "type nonDispatchableHandle uint64\n")

	// inject the bool translate methods used in commands,
	gen.write(s, "\n")
	gen.write(s, "// goBool32 is a type conversion function for special handling of Bool32 to bool.\n")
	gen.write(s, "func goBool32(val Bool32) bool { return val != Bool32(FALSE) }\n\n")
	gen.write(s, "// vkBool32 is a type conversion function for special handling of bool to Bool32.\n")
	gen.write(s, "func vkBool32(val bool) Bool32 {\n")
	gen.write(s, "	if val {\n")
	gen.write(s, "		return Bool32(TRUE)\n")
	gen.write(s, "	}\n")
	gen.write(s, "	return Bool32(FALSE)\n")
	gen.write(s, "}\n")
}

// Write interface for a feature and tag generated features as having been done.
func (gen *generator) beginFeature(feat *etree.Element, emit bool) {
	gen.feature = feat.SelectAttrValue("name", "")

	// different platforms need different files.
	gen.file = baseFile
	platform := feat.SelectAttrValue("platform", "")
	if platform != "" {
		gen.file = "vk_" + platform
	}
	slog.Debug("writing feature", "name", gen.feature, "file", gen.file)
}

// Finish an interface file, closing it when done.
func (gen *generator) endFeature() {
	gen.feature = ""
	gen.file = "vk"
}

// called once at the end of code generation.
func (gen *generator) endFile() {
	for filename, sections := range gen.out {
		content := strings.Builder{}

		// initialize the file based on platform.
		file := filepath.Join(bindingDir, filename+".go")

		// write the sections in order.
		for _, section := range sectionOrder {
			sectionContent := sections[section]
			if sectionContent == nil || sectionContent.Len() == 0 {
				continue // commands are generated later.
			}
			slog.Debug("writing section", "file", file, "name", section)
			if section != includeSection {
				content.WriteString(sectionBreak(section))
			}

			// end the enum const block.
			if section == enumSection {
				content.WriteString("\nconst (\n")
			}
			content.WriteString(sectionContent.String())
			if section == enumSection {
				content.WriteString(")\n")
			}
		}
		os.WriteFile(file, []byte(content.String()), 0644)
	}

	// format the output files.
	cmd := exec.Command("go", "fmt", "./vk")
	output, err := cmd.CombinedOutput()
	if err != nil {
		slog.Error("Error formatting files", "error", string(output))
	}
}

// write is a helper method to write strings to output buffers.
func (gen *generator) write(section, text string) {
	gen.out[gen.file][section].WriteString(text)
}

// sectionBreak returns a line comment for a section.
func sectionBreak(name string) string {
	pad := 70 - len(name)
	return fmt.Sprintf("\n// %s %s\n\n", name, strings.Repeat("=", pad))
}

// docLink returns a command that is a link to the Vulkan documentation.
func docLink(tag, name string) string {
	return fmt.Sprintf("// %s: https://docs.vulkan.org/refpages/latest/refpages/source/%s.html", tag, name)
}
