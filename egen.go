// generator captures output.
type generator struct {
	feature string // feature being generated.

	// output file
	// - "vk"       : platform independent consts, enums, types and structs.
	// - "vk_cgo"   : cgo commands
	// - "vk_sys"   : syscall commands
	// - "vk_*"     : platform features including cgo|syscall commands
	file string // output file.  "base" when there is no platform.

	// generator output by [file][section].
	out map[string]map[string]*strings.Builder
}

// newGenerator initializes the output buffers.
func newGenerator() *generator {
	gen := &generator{}
	gen.out = map[string]map[string]*strings.Builder{}

	// initialize the output buffers.
	files := []string{baseFile, cgoFile, syscallFile}
	for _, p := range platforms {
		files = append(files, "vk_"+p)
	}
	for _, f := range files {
		gen.out[f] = map[string]*strings.Builder{}
		for _, s := range sectionOrder {
			gen.out[f][s] = &strings.Builder{}
		}
		o := gen.out[f][includeSection]
		o.WriteString("// Code generated by vkgen from vk.xml. DO NOT EDIT.\n\n")
		switch f {
		case baseFile:
			o.WriteString("package vk\n\n")
			o.WriteString("import (\n")
			o.WriteString("	\"unsafe\"\n")
			o.WriteString("	\"fmt\"\n")
			o.WriteString(")\n")
		case cgoFile:
			o.WriteString("//go:build !windows\n\n")
			o.WriteString("package vk\n\n")
			o.WriteString("import (\n")
			o.WriteString("	\"unsafe\"\n")
			o.WriteString(")\n")
		case syscallFile:
			o.WriteString("//go:build windows\n\n")
			o.WriteString("package vk\n\n")
			o.WriteString("import (\n")
			o.WriteString("	\"syscall\"\n")
			o.WriteString("	\"unsafe\"\n")
			o.WriteString(")\n")
		case "vk_win32":
			o.WriteString("//go:build windows\n\n")
			o.WriteString("package vk\n\n")
			o.WriteString("import (\n")
			o.WriteString("	\"syscall\"\n")
			o.WriteString("	\"unsafe\"\n\n")
			o.WriteString("	\"golang.org/x/sys/windows\"\n")
			o.WriteString(")\n")
		case "vk_metal":
			o.WriteString("//go:build darwin || ios\n\n")
			o.WriteString("package vk\n\n")
			o.WriteString("import (\n")
			o.WriteString("	\"unsafe\"\n")
			o.WriteString(")\n")
		}
	}

	// add some base metal types
	if slices.Contains(platforms, "metal") {
		gen.file = "vk_metal"
		gen.write(basetypeSection, "\n")
		gen.write(basetypeSection, "type CAMetalLayer struct{}\n")
		gen.write(basetypeSection, "type IOSurfaceRef unsafe.Pointer\n")
		gen.write(basetypeSection, "type MTLBuffer_id unsafe.Pointer\n")
		gen.write(basetypeSection, "type MTLCommandQueue_id unsafe.Pointer\n")
		gen.write(basetypeSection, "type MTLDevice_id unsafe.Pointer\n")
		gen.write(basetypeSection, "type MTLSharedEvent_id unsafe.Pointer\n")
		gen.write(basetypeSection, "type MTLTexture_id unsafe.Pointer\n")
	}
	gen.file = baseFile
	return gen
}

// internal sections for document output.
const (
	includeSection  = "include"  //
	defineSection   = "define"   //
	basetypeSection = "basetype" //
	enumSection     = "enum"     //
	structSection   = "struct"   //
	bitmaskSection  = "bitmask"  //
	groupSection    = "group"    // enum's
	cmdSection      = "command"  //

	// output file names
	baseFile    = "vk"         // platform independent consts, enums, types and structs.
	cgoFile     = "vk_cgo"     // cgo commands
	syscallFile = "vk_syscall" // syscall commands
)

